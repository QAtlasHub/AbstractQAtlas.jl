# relations/interface.jl вЂ” the uniform three-verb contract for relations,
# the @relation declaration macro, and the adoption surface.
#
# A relation is an *identity among observables or exponents* вЂ” a generic,
# model-independent statement.  Each is DECLARED EXACTLY ONCE via
# [`@relation`](@ref); everything else (solving for any affine variable,
# checking, ОІ-or-T normalization, registry membership, introspection) is
# derived mechanically.  No hand-written rearrangements, no per-relation
# boilerplate.
#
# Architecture:
#
#   public verbs   residual / check / solve      (ОІ-or-T normalization, one place)
#   kernels        _residual (ONE per relation)  |  _solve (rare, non-affine only)
#   traits         variables(rel), domain(rel)
#   registry       all_relations / applicable_relations / relation_report / check_all

"""
    AbstractRelation

Abstract parent type for physics relations.  Concrete relations are
declared with [`@relation`](@ref) and implement the three verbs:

- [`residual`](@ref)`(rel; vars...)` вЂ” signed violation; `0` в‡” satisfied.
- [`check`](@ref)`(rel; atol=0, vars...)` вЂ” `|residual| в‰¤ atol`.
- [`solve`](@ref)`(rel, Val(:x); vars...)` вЂ” the value of `x` implied by
  the remaining variables.

**Exact-arithmetic contract**: `residual` and `solve` must not promote
their inputs вЂ” `Rational` in в‡’ `Rational` out, so exactly-known values
(e.g. the 2D Ising exponents) satisfy their relations *exactly*
(`residual == 0//1`), not merely to floating-point tolerance.

Relations whose variables include an inverse temperature accept either
`ОІ` or `T` (exactly one) at every public verb; the normalization happens
once, in the verb layer вЂ” kernels only ever see `ОІ`.
"""
abstract type AbstractRelation end
export AbstractRelation

# the residual kernel generic function вЂ” one method per relation,
# generated by @relation.  Public code calls `residual`, never this.
function _residual end

# в”Ђв”Ђв”Ђ Traits в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

"""
    variables(rel::AbstractRelation) -> Tuple{Vararg{Symbol}}

The *required* variables of the relation (optional variables with
defaults, e.g. a site count `N = 1`, are not listed).  Filled in by
[`@relation`](@ref); used for applicability matching in
[`applicable_relations`](@ref) and by the ОІ-or-T normalizer.
"""
function variables end
export variables

"""
    domain(rel::AbstractRelation) -> Symbol

The relation's family tag (`:scaling`, `:thermodynamic`,
`:fundamental`, `:topology`, вЂ¦) вЂ” for filtering in the registry API,
and for disambiguating same-named variables across families (the
exponent `ОІ` vs the inverse temperature `ОІ`).
"""
function domain end
export domain

# в”Ђв”Ђв”Ђ Registry в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

const _RELATION_REGISTRY = AbstractRelation[]

"""
    all_relations(; domain=nothing) -> Vector{AbstractRelation}

Every relation declared with [`@relation`](@ref) in this package,
optionally filtered by [`domain`](@ref).  The registry is what makes the
package *adoptable in one call*: a consumer never hand-lists relations
(see [`relation_report`](@ref)).
"""
function all_relations(; domain::Union{Nothing,Symbol}=nothing)
    domain === nothing && return copy(_RELATION_REGISTRY)
    return filter(r -> AbstractQAtlas.domain(r) === domain, _RELATION_REGISTRY)
end
export all_relations

# в”Ђв”Ђв”Ђ ОІ-or-T normalization (single point) в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

# `solving` is the solve-target variable (or `nothing` for residual/
# check): when ОІ itself is being solved for, the ОІ-or-T demand is waived
# вЂ” the probes supply it.  (Physics overloads ОІ: the same normalization
# serves the inverse temperature and steps aside for the exponent.)
function _normalize_kwargs(
    rel::AbstractRelation, kwargs::NamedTuple; solving::Union{Nothing,Symbol}=nothing
)
    :ОІ in variables(rel) || return kwargs
    hasОІ = haskey(kwargs, :ОІ)
    hasT = haskey(kwargs, :T)
    if solving === :ОІ
        (hasОІ || hasT) && error("cannot pass ОІ or T while solving for :ОІ")
        return kwargs
    end
    hasОІ && hasT && error("pass either ОІ or T, not both")
    hasОІ && return kwargs
    hasT || error("pass either ОІ or T")
    rest = Base.structdiff(kwargs, NamedTuple{(:T,)})
    return merge(rest, (ОІ=1 / kwargs.T,))
end

# в”Ђв”Ђв”Ђ Public verbs в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

"""
    residual(rel::AbstractRelation; vars...) -> Number

Signed violation of the relation at the given variable values; zero if
and only if the relation is satisfied.  Preserves the input number types
(see the exact-arithmetic contract on [`AbstractRelation`](@ref)).
"""
function residual(rel::AbstractRelation; kwargs...)
    return _residual(rel; _normalize_kwargs(rel, values(kwargs))...)
end
export residual

"""
    check(rel::AbstractRelation; atol=0, vars...) -> Bool

`abs(residual(rel; vars...)) в‰¤ atol`.  With the default `atol = 0` this
is an *exact* test вЂ” appropriate for `Rational` inputs; pass an explicit
`atol` for floating-point / error-bar data.
"""
check(rel::AbstractRelation; atol=0, kwargs...) = abs(residual(rel; kwargs...)) <= atol
export check

"""
    AbstractInequality <: AbstractRelation

A relation asserting an **inequality** rather than an equality.  Its
[`residual`](@ref) is the **slack in the `в‰Ґ 0` form**: the relation holds
iff `residual в‰Ґ 0` (within tolerance), so [`check`](@ref) tests that
direction instead of `abs(residual) в‰¤ atol`.  [`solve`](@ref) still
returns the **saturation** value вЂ” where the slack vanishes, i.e. the
tight bound (e.g. `solve(Subadditivity(), Val(:S_AB))` gives the maximum
`S_A + S_B`).  Declared with [`@inequality`](@ref).
"""
abstract type AbstractInequality <: AbstractRelation end
export AbstractInequality

# an inequality holds when its slack is non-negative (float noise: в‰Ґ в€’atol)
check(rel::AbstractInequality; atol=0, kwargs...) = residual(rel; kwargs...) >= -atol

"""
    slack(ineq::AbstractInequality; vars...) -> Number

The non-negativity margin of an inequality вЂ” its [`residual`](@ref): how
far from saturation, negative iff the inequality is violated.
"""
slack(rel::AbstractInequality; kwargs...) = residual(rel; kwargs...)
export slack

# whether a relation passes on the given (already-normalized) data вЂ” the
# polymorphic per-kind test used by `relation_report`/`check_all`, so an
# `AbstractInequality` is judged by its `в‰Ґ 0` direction, not `abs в‰¤ atol`.
_passes(rel::AbstractRelation, r, atol) = abs(r) <= atol
_passes(rel::AbstractInequality, r, atol) = r >= -atol

"""
    solve(rel::AbstractRelation, ::Val{x}; vars...) -> Number

The value of variable `x` implied by the relation and the remaining
variables, e.g. `solve(Widom(), Val(:Оі); ОІ=1//8, Оґ=15//1) == 7//4`.

No per-variable rearrangements are hand-written: for any variable the
relation is *affine* in (true of almost every identity here), the answer
follows exactly from three kernel evaluations вЂ” see the generic
`_solve`.  A relation that is non-affine in some variable provides a
specialized `_solve` for it (e.g. `FreeEnergyFromZ` for `Z`); attempting
a generic solve for a non-affine variable throws instead of silently
returning a wrong value.  Preserves input number types.
"""
function solve(rel::AbstractRelation, v::Val{X}; kwargs...) where {X}
    return _solve(rel, v; _normalize_kwargs(rel, values(kwargs); solving=X)...)
end
export solve

# Generic solve: for a residual r(x) affine in x, r(x) = rв‚Ђ + (rв‚Ѓ в€’ rв‚Ђ)x
# exactly, so the root is x* = в€’rв‚Ђ/(rв‚Ѓ в€’ rв‚Ђ).  Three probes (x = 0, 1, 2)
# both solve and VERIFY affinity: the parabola test rв‚‚ в€’ 2rв‚Ѓ + rв‚Ђ must
# vanish вЂ” exactly for exact inputs (Integer/Rational), to a relative
# 1e-8 for inexact ones (float OR complex-float вЂ” `abs` handles both).
# Integer probes promote correctly, so Rational data stays Rational.
function _solve(rel::AbstractRelation, ::Val{X}; kwargs...) where {X}
    kw = values(kwargs)
    at(v) = _residual(rel; merge(kw, NamedTuple{(X,)}((v,)))...)
    r0, r1, r2 = at(0), at(1), at(2)
    curv = r2 - 2r1 + r0
    isaffine = if curv isa Union{Integer,Rational}
        iszero(curv)                              # exact types: exact test
    elseif !all(isfinite, (r0, r1, r2))
        # A non-finite probe (a 1/x term evaluated at an integer probe, x=0)
        # means the residual blows up вЂ” not safely affine-solvable here.
        # Guard before `scale` goes to Inf and silently defeats the test.
        false
    else
        scale = max(abs(r0), abs(r1), abs(r2))    # float / complex: relative tol
        abs(curv) <= 1e-8 * max(scale, oneunit(scale))
    end
    isaffine || error(
        "solve: $(typeof(rel)) is not affine in :$X (or blows up at an integer " *
        "probe) вЂ” define a specialized _solve(::$(typeof(rel)), ::Val{:$X}; ...) " *
        "for this variable",
    )
    b = r1 - r0
    iszero(b) && error("solve: $(typeof(rel)) does not depend on :$X")
    return -r0 / b
end

# в”Ђв”Ђв”Ђ The declaration macro в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

"""
    @relation :domain Name(x, y, z, opt=default) = expr

Declare a relation ONCE.  Expands to the complete implementation:

- `struct Name <: AbstractRelation end` (docstring-attachable),
- the residual kernel `_residual(::Name; x, y, z, opt=default) = expr`
  (kernels tolerate extra keywords, so a whole data set can be splatted
  through [`relation_report`](@ref)),
- `variables(::Name) = (:x, :y, :z)` вЂ” required variables only,
- `domain(::Name) = :domain`,
- registry insertion and `export Name`.

`residual`/`check` work immediately; `solve` works for every variable
the expression is affine in.  Note for downstream packages declaring
their own relations: the struct and methods precompile fine, but the
registry insertion is a load-time side effect вЂ” re-register from your
module `__init__` if you need registry visibility across sessions.

```julia
@relation :scaling Rushbrooke(О±, ОІ, Оі) = О± + 2ОІ + Оі - 2
```
"""
# Shared code generation for `@relation` (equality) and `@inequality`
# (в‰Ґ 0 slack).  `super` is the supertype the generated struct subtypes,
# `what` names the macro for error messages.
function _build_relation(dom, defn, super::Symbol, what::String)
    Meta.isexpr(defn, :(=), 2) || error("$what expects `Name(vars...) = expr`, got $defn")
    call, body = defn.args
    Meta.isexpr(call, :call) || error("$what: left side must be a call, got $call")
    name = call.args[1]
    name isa Symbol || error("$what: relation name must be a Symbol")
    required = Symbol[]
    kwargsig = Any[]
    for p in call.args[2:end]
        if p isa Symbol
            push!(required, p)
            push!(kwargsig, esc(p))
        elseif Meta.isexpr(p, :(=), 2) || Meta.isexpr(p, :kw, 2)
            push!(kwargsig, Expr(:kw, esc(p.args[1]), esc(p.args[2])))
        else
            error("$what: cannot parse parameter $p")
        end
    end
    # kernels slurp extra keywords so relation_report can splat full data
    push!(kwargsig, Expr(:..., esc(gensym("extra"))))
    ename = esc(name)
    # NB: every generated method is module-QUALIFIED вЂ” short-form
    # definitions inside a macro quote are otherwise hygienized into
    # gensym locals, and qualification also makes the macros usable from
    # downstream modules (methods land on OUR generics, on THEIR type вЂ”
    # the intended extension pattern, not piracy).
    kernel_sig = Expr(
        :call, :(AbstractQAtlas._residual), Expr(:parameters, kwargsig...), :(::$ename)
    )
    kernel = Expr(:function, kernel_sig, esc(body))
    reqtuple = Expr(:tuple, (QuoteNode(s) for s in required)...)
    domval = QuoteNode(dom isa QuoteNode ? dom.value : dom)
    supertype = :(AbstractQAtlas.$super)
    return quote
        Core.@__doc__ struct $ename <: $supertype end
        $kernel
        AbstractQAtlas.variables(::$ename) = $reqtuple
        AbstractQAtlas.domain(::$ename) = $domval
        push!(AbstractQAtlas._RELATION_REGISTRY, $ename())
        $(Expr(:export, name))
        $ename
    end
end

macro relation(dom, defn)
    return _build_relation(dom, defn, :AbstractRelation, "@relation")
end
export var"@relation"

"""
    @inequality :domain Name(x, y, z, opt=default) = expr

Declare an INEQUALITY relation, `expr в‰Ґ 0`.  Identical to
[`@relation`](@ref) but the generated struct subtypes
[`AbstractInequality`](@ref): `residual` returns the slack `expr`,
`check` tests `expr в‰Ґ в€’atol`, and `solve` returns the saturation value.

```julia
@inequality :entanglement Subadditivity(S_A, S_B, S_AB) = S_A + S_B - S_AB
```
"""
macro inequality(dom, defn)
    return _build_relation(dom, defn, :AbstractInequality, "@inequality")
end
export var"@inequality"

# в”Ђв”Ђв”Ђ Adoption surface: one-call verification over a data set в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

function _normalize_data(data::NamedTuple)
    return haskey(data, :T) && !haskey(data, :ОІ) ? merge(data, (ОІ=1 / data.T,)) : data
end

"""
    applicable_relations(data::NamedTuple; domain=nothing) -> Vector{AbstractRelation}

The registered relations whose required [`variables`](@ref) are all
present in `data` (after ОІ-or-T normalization), optionally filtered by
[`domain`](@ref).  This is how a consumer package discovers what can be
checked against its observables вЂ” no hand-listing.

Pass `domain` when mixing families in one data set: variable names are
matched literally, and physics overloads symbols across families (the
critical exponent `ОІ` vs the inverse temperature `ОІ`).
"""
function applicable_relations(data::NamedTuple; domain::Union{Nothing,Symbol}=nothing)
    d = _normalize_data(data)
    ks = keys(d)
    return filter(r -> all(v -> v in ks, variables(r)), all_relations(; domain=domain))
end
export applicable_relations

"""
    relation_report(data::NamedTuple; atol=0, domain=nothing)
        -> Vector{@NamedTuple{relation::AbstractRelation, residual::Number, pass::Bool}}

Evaluate every applicable relation against `data` and report per-relation
residuals.  The one-call integration point for downstream packages:

```julia
# gate an exponent table (an atlas registry, MC-extracted exponents, вЂ¦):
relation_report((; О±=0//1, ОІ=1//8, Оі=7//4, Оґ=15//1, ОЅ=1//1, О·=1//4, d=2))

# cross-check measured thermodynamics against every applicable identity:
relation_report((; C=c, var_E=v, ОІ=ОІ, N=N); atol=tol)
```
"""
function relation_report(data::NamedTuple; atol=0, domain::Union{Nothing,Symbol}=nothing)
    d = _normalize_data(data)
    out = @NamedTuple{relation::AbstractRelation, residual::Number, pass::Bool}[]
    for rel in applicable_relations(d; domain=domain)
        r = _residual(rel; d...)
        push!(out, (relation=rel, residual=r, pass=_passes(rel, r, atol)))
    end
    return out
end
export relation_report

"""
    check_all(data::NamedTuple; atol=0, domain=nothing) -> Bool

`true` iff every applicable relation passes on `data` вЂ” and at least one
relation applies: an empty match is `false`, never a silent green.
"""
function check_all(data::NamedTuple; atol=0, domain::Union{Nothing,Symbol}=nothing)
    report = relation_report(data; atol=atol, domain=domain)
    return !isempty(report) && all(row -> row.pass, report)
end
export check_all
